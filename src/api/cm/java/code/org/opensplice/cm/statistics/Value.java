package org.opensplice.cm.statistics;

public class Value extends AbstractValue{
    protected long value;
    
    public Value(String name, long value) {
        super(name);
        this.value = value;
    }

    public long getValue() {
        return value;
    }

    public void setValue(long value) {
        this.value = value;
    }
}
